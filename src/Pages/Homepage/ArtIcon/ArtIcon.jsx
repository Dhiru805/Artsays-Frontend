import { useState, useEffect } from "react";
import { useNavigate } from "react-router-dom";
import { ImArrowUpRight2 } from "react-icons/im";
import getAPI from "../../../api/getAPI";

const ArtIcon = () => {
  const [data, setData] = useState(null);
  const [celebrities, setCelebrities] = useState([]);
  const [loading, setLoading] = useState(true);
  const navigate = useNavigate();

  useEffect(() => {
    const fetchData = async () => {
      try {
        const pageRes = await getAPI("/api/homepage/published");
        const homepage = pageRes.data.data;
        if (!homepage?._id) throw new Error("No published homepage found");

        const sectionRes = await getAPI(
          `/api/homepage-sections/art-icon/${homepage._id}`
        );
        if (!sectionRes.data.success || !sectionRes.data.data)
          throw new Error("Art Icon section not found");

        setData(sectionRes.data.data);

        const celebRes = await getAPI("/api/celebrities");
        if (!celebRes?.hasError) {
          setCelebrities(celebRes.data.data || []);
        } else {
          console.log("Error fetching celebrities:", celebRes);
        }
      } catch (err) {
        console.error(err);
      } finally {
        setLoading(false);
      }
    };

    fetchData();
  }, []);

  const slugify = (text) => {
    return text
      .toLowerCase()
      .trim()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-+|-+$/g, "");
  };

  if (loading) return <div>Loading...</div>;
  if (!data) return <div>No Art Icon section available</div>;

  return (
    <div className="bg-[#F8F8F8]">
      <div className="max-w-[1440px] mx-auto py-4 px-3">
       
        <div className="grid grid-cols-1 md:grid-cols-4 gap-6 mt-3 items-center">
          <h1 className="md:col-span-3 text-lg md:text-4xl font-bold text-[#6F4D34] px-3">
            {data.heading}
          </h1>
          {data.buttonName && (
            <a
              href={data.buttonLink || "#"}
              className="hidden md:inline-flex items-center justify-center bg-red-500 text-white py-2 px-4 rounded-full font-semibold shadow-md transition hover:bg-red-600"
            >
              {data.buttonName}
            </a>
          )}
        </div>

        <hr className="my-3 border-dark" />

        
        <p className="mt-3 text-xs md:text-lg font-medium text-black leading-relaxed px-3">
          {data.description}
        </p>

        <div className="grid grid-cols-2 sm:grid-cols-2 lg:grid-cols-3 gap-3 mt-6">
          {celebrities.length > 0 ? (
            celebrities.map((celebrity) => (
              <div
                key={celebrity._id}
                className="w-full mx-auto rounded-[2rem] overflow-hidden flex flex-col border-2 border-[#48372D] bg-[#EBEBEB]"
              >
                <div className="px-2 py-4 text-center">
                  <h2 className="text-sm md:text-2xl font-extrabold text-[#4A3426]">
                    {celebrity.artistName || ""}
                  </h2>
                </div>

                <div className="w-full h-40 sm:h-64 rounded-[2rem] border-t-2 border-[#48372D] overflow-hidden flex items-center justify-center">
                  <img
                    src={celebrity.image || "/herosectionimg/1.jpg"}
                    alt={celebrity.artistName || ""}
                    className="w-full h-40 sm:h-64 object-contain"
                  />
                </div>

                <div className="relative hidden md:block">
                  <div className="absolute bottom-6 left-1/2 transform -translate-x-1/2">
                    <button
                      onClick={() =>
                        navigate(`/celebrity/${slugify(celebrity.artistName)}`, {
                          state: { celebrity },
                        })
                      }
                      className="flex items-center gap-2 bg-white/70 backdrop-blur-md text-gray-800 font-medium px-3 py-2 rounded-full shadow-md hover:bg-white transition"
                    >
                      View Collection
                      <span className="text-white text-lg">
                        <ImArrowUpRight2 className="bg-black rounded-full h-8 w-8 p-2" />
                      </span>
                    </button>
                  </div>
                </div>
              </div>
            ))
          ) : (
            <div>No celebrities available</div>
          )}
        </div>
      </div>
    </div>
  );
};

export default ArtIcon;
