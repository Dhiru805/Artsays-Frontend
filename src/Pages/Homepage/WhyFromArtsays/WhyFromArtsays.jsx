import { useState, useEffect } from "react";
import getAPI from "../../../api/getAPI";
import { LiaCoinsSolid } from "react-icons/lia";

const WhyFromArtsays = () => {
  const [data, setData] = useState(null);
  const [loading, setLoading] = useState(true);
  const imageBaseURL = process.env.REACT_APP_API_URL_FOR_IMAGE;

  useEffect(() => {
    const fetchData = async () => {
      try {
        const pageRes = await getAPI("/api/homepage/published");
        const homepage = pageRes.data.data;
        if (!homepage?._id) throw new Error("No published homepage found");

        const sectionRes = await getAPI(
          `/api/homepage-sections/why-buy-artsays/${homepage._id}`
        );
        if (!sectionRes.data.success || !sectionRes.data.data)
          throw new Error("Why From Artsays section not found");

        setData(sectionRes.data.data);
      } catch (err) {
        console.error(err);
      } finally {
        setLoading(false);
      }
    };

    fetchData();
  }, []);

  if (loading) return <div>Loading...</div>;
  if (!data) return <div>No 'Why Buy from Artsays' section available</div>;

  return (
    <div className="bg-[#F8F8F8]">
      <div className="max-w-[1440px] mx-auto py-4 px-3">
        <div className="grid grid-cols-1 md:grid-cols-4 gap-6 mt-3">
          <h1 className="md:col-span-3 text-lg md:text-4xl font-bold text-[#6F4D34] px-3">
            {data.heading}
          </h1>
          {data.buttonName && (
            <a
              href={data.buttonLink || "#"}
              className="hidden md:inline-flex items-center justify-center bg-red-500 text-white font-semibold rounded-full shadow px-6 py-2 text-center"
            >
              {data.buttonName}
            </a>
          )}
        </div>

        <hr className="my-3 border-dark" />

        <p className="mt-3 text-xs md:text-base font-medium text-black leading-relaxed px-3">
          {data.description}
        </p>

        <div className="grid grid-cols-2 sm:grid-cols-2 lg:grid-cols-3 gap-3 py-4">
          {data.cards?.map((card, idx) => (
            <div key={idx} className="group w-full h-64 [perspective:1000px]">
              <div className="relative w-full h-full transition-transform duration-700 [transform-style:preserve-3d] group-hover:[transform:rotateY(180deg)]">
                
                <div className="absolute inset-0 flex flex-col items-center justify-center rounded-xl bg-white shadow-lg [backface-visibility:hidden]">
                  {card.icon ? (
                    <img
                      src={`${imageBaseURL}/${card.icon}`}
                      alt={card.heading}
                      className="w-36 h-36 mb-3 object-contain"
                    />
                  ) : (
                    <LiaCoinsSolid className="text-orange-500 text-[7rem] mb-3" />
                  )}
                  <h2 className="text-lg md:text-xl font-semibold text-brown-900 text-center">
                    {card.heading}
                  </h2>
                </div>

                <div className="absolute inset-0 flex items-center justify-center rounded-xl bg-[#4a3423] px-2 md:px-6 text-center text-white [transform:rotateY(180deg)] [backface-visibility:hidden]">
                  <p className="!text-xs md:!text-base leading-relaxed">
                    {card.description}
                  </p>
                </div>
              </div>
            </div>
          ))}
        </div>
      </div>
    </div>
  );
};

export default WhyFromArtsays;
